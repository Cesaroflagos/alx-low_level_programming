#include <stdio.h>

/**
 * add - Starting point
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Starting point
 * @a: first int
 * @b: second int
 * Return:Subtracted final result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - Starting point
 * @a: first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - Starting point
 * @a: first int
 * @b: second int
 * Return: final result
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - Starting point
 * @a: first int
 * @b: second int
 * Return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}
